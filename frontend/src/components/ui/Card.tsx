export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { hoverEffect?: boolean }
>(({ className, hoverEffect = false, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      'rounded-2xl border border-slate-800/80 bg-slate-950/45 backdrop-blur-md text-slate-100 shadow-xl transition-all duration-300',
      hoverEffect && 'hover:border-emerald-500/40 hover:shadow-emerald-950/10 hover:translate-y-[-2px]',
      className
    )}
    {...props}
  />
));
Card.displayName = 'Card';

